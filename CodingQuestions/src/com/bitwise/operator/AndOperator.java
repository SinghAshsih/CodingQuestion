package com.bitwise.operator;

public class AndOperator {
	public static void main(String[] args) {
		// and operator &
		// or operator | (pipe)
		// xor ^
		// not ~
		// leftShift <<
		// rightshift >>
		// unsigned rightshift >>>
		int c = 22 & 55;
		System.out.println(Integer.toBinaryString(22));
		System.out.println(Integer.toBinaryString(55));
		System.out.println(c);

	}
}
